package com.example.trackme.utiils;

public class ListLokasiPengguna {
    private String name;
    private String url;

    public  ListLokasiPengguna(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

}
